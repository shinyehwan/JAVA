package operator;

public class OperatorEx2 {

	public static void main(String[] args) {

		int score = 100;
		
		System.out.println(++score);
		//score = score+1;
		//score += 1;
		
		System.out.println(score++);
		System.out.println(score);
		System.out.println(--score);
		System.out.println(score--);
		System.out.println(score);
	}

}
